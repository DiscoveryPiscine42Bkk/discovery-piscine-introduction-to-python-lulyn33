#!/usr/bin/env python3
first_num = int(input('Give me the first number: '))
second_num = int(input('Give me the second number: '))
print('Thank you!')

plus = first_num + second_num 
minus = first_num - second_num
div = first_num // second_num
mult = first_num * second_num

print(f"{first_num} + {second_num} = {plus}")
print(f"{first_num} - {second_num} = {minus}")
print(f"{first_num} / {second_num} = {div}")
print(f"{first_num} * {second_num} = {mult}")
